#include "3-calc.h"
/**
 * op_add - Entry point
 * @a: 1st Num
 * @b: 2nd Num
 *
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - Entry point
 * @a: 1st Num
 * @b: 2nd Num
 *
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - Entry point
 * @a: 1st Num
 * @b: 2nd Num
 *
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - Entry point
 * @a: 1st Num
 * @b: 2nd Num
 *
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Entry point
 * @a: 1st Num
 * @b: 2nd Num
 *
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
